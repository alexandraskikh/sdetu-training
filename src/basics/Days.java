package basics;

public class Days {
	
	public static void main (String [] args) {
		
		
		String daysOfWeek="Tuesday";
		
		switch (daysOfWeek) {
		
		case "Monday" :
			
			System.out.println ("Today is Monday");
			
			
         case "Tuesday" :
			
			System.out.println ("Today is Tuesday");
			
			break;
         case "Wednesday" :
 			
 			System.out.println ("Today is Wednesday");
 			
			
			
		
		}
	}

}
